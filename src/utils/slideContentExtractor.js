// Export individual slide content if needed
export const getSlideContent = (slideNumber) => {
  const allContent = extractSlideContent();
  const slidePattern = new RegExp(`שקף ${slideNumber}[^]*?(?=שקף \\d+|$)`, "g");
  const match = allContent.match(slidePattern);
  return match ? match[0] : "";
};
